// Años perro
// Crear un programa que pida ingresar una edad y
// devuelva el equivalente de esas edad en años perro.

const edad = prompt('¿Qué edad tienes?');

const añosPerro = 7;

const resultados = edad * añosPerro;

alert('Tu edad en años perro es ' + resultados); // Concatenacion

alert(`Tu edad en años perro es ${resultados} !!!`); // template string